DROP TABLE IF EXISTS user;
DROP TABLE IF EXISTS post;

CREATE TABLE user (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  username TEXT UNIQUE NOT NULL,
  password TEXT NOT NULL
);

CREATE TABLE makes
(
    id   INTEGER NOT NULL
        CONSTRAINT MAKES_PK
            PRIMARY KEY AUTOINCREMENT,
    name TEXT    NOT NULL
);

CREATE UNIQUE INDEX MAKES_ID_UINDEX
    ON makes (id);

create table bodytypes
(
    id   integer not null
        constraint bodytypes_pk
            primary key autoincrement,
    name text    not null
);

create unique index bodytypes_id_uindex
    on bodytypes (id);

create table enginetypes
(
    id   integer not null
        constraint enginetypes_pk
            primary key autoincrement,
    name text    not null
);

create unique index enginetypes_id_uindex
    on enginetypes (id);


create table fueltypes
(
    id   integer not null
        constraint fueltypes_pk
            primary key autoincrement,
    name text    not null
);

create unique index fueltypes_id_uindex
    on fueltypes (id);

create table models
(
    id     serial
        constraint models_pk
            primary key,
    makeid int  not null,
    model  text not null
);

create unique index models_id_uindex
    on models (id);



create unique index models_name_uindex
    on models (name);


create table transmissions
(
    id   integer not null
        constraint transmissions_pk
            primary key autoincrement,
    name text    not null
);

create unique index transmissions_id_uindex
    on transmissions (id);

create unique index transmissions_name_uindex
    on transmissions (name);

create table color
(
    id   integer not null
        constraint color_pk
            primary key autoincrement,
    name text    not null
);

create unique index color_id_uindex
    on color (id);

create unique index color_name_uindex
    on color (name);



